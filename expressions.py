"""
Totem Face Bitmap Library
=========================

Centralized collection of 8x8 face grids for the MAX7219 LED matrix.
Each grid is a list of 8 rows, each row a list of 8 pixels (0=off, 1=on).

Coordinate system:
  - (0,0) is top-left
  - x increases rightward (columns 0-7)
  - y increases downward (rows 0-7)

Eyes are handled by the ultrasonic sensor. This matrix displays mouth only.
Rows 2-5 (inner area) are the mouth zone; rows 0-1 and 6-7 are face outline.
"""

